function makeStage(){
    var y = 0;
    //vaitse seuraavalla rivillä kenttä.
    battlefield.forEach(function(rivi){
        y++;
        for(x = 0;x < rivi.length;x++){
            var merkki = rivi.charAt(x);
            if(merkki == 'X') { BlokkiaLisaa(x,y); }
        }
    });
}

